    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        if (jTable1.getRowCount() > 0) {
            Product product = AccessProduct.getProductAt(selectedRow);
            if (product != null) {
                selectedProductTf.setText(product.getModel() + " " + product.getType());
                selectedProduct = product;
                tfBrand.setText(product.getBrand());
                tfPrice.setText(String.valueOf(product.getPrice()));
                tfModel.setText(product.getModel());
                tfQty.setText(String.valueOf(product.getQty()));
                tfType.setText(product.getType());
            } else {
                selectedProductTf.setText("None");
                JOptionPane.showMessageDialog(this, "Invalid Selection", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_jTable1MouseClicked

