    private void btnCloneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCloneActionPerformed
        if (selectedProduct != null) {
            if (!tfBrand.getText().isEmpty() && !tfModel.getText().isEmpty() && !tfType.getText().isEmpty() && !tfPrice.getText().isEmpty() && !tfQty.getText().isEmpty()) {
                if (AccessProduct.getAll().size() > 0) {
                    Product clonedProduct = selectedProduct.clone();
                    AccessProduct.saveProduct(clonedProduct);
                    loadTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Cannot Clone unadded Data", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Form is Incomplete", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Select a product first", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCloneActionPerformed

    private void loadTable() {
        dtm.setRowCount(0);
        for (Product p : AccessProduct.getAll()) {
            int rowCount = jTable1.getRowCount() + 1;
            Vector row = new Vector<>();
            row.add(String.valueOf(rowCount));
            row.add(p.getBrand());
            row.add(p.getModel());
            row.add(p.getType());
            row.add(p.getQty());
            row.add(p.getPrice());
            dtm.addRow(row);
        }
    }

