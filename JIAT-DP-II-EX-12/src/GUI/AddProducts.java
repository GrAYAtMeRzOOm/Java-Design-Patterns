    private void btnBuildActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuildActionPerformed
        if (selectedProduct != null) {
            if (!tfBrand.getText().isEmpty() && !tfModel.getText().isEmpty() && !tfType.getText().isEmpty() && !tfPrice.getText().isEmpty() && !tfQty.getText().isEmpty()) {
                if (AccessProduct.getAll().size() > 0) {

                    String brand = tfBrand.getText();
                    String model = tfModel.getText();

                    String type = tfType.getText();
                    Double price = Double.valueOf(tfPrice.getText());
                    int qty = Integer.valueOf(tfQty.getText());

                    Product.ProductBuilder computerBuilder = new Product.ProductBuilder();
                    Product product = computerBuilder
                            .setId(selectedProduct.getId())
                            .setBrand(brand)
                            .setModel(model)
                            .setPrice(price)
                            .setType(type)
                            .setQty(qty)
                            .build();
                    AccessProduct.updateProduct(product);
                    selectedProductTf.setText("None");
                    loadTable();
                    tfBrand.setText("");
                    tfPrice.setText("");
                    tfModel.setText("");
                    tfQty.setText("");
                    tfType.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Cannot Update unadded Data", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Form is Incomplete", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Select a product first", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuildActionPerformed

    private void btnBuild1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuild1ActionPerformed
        if (!tfBrand.getText().isEmpty() && !tfModel.getText().isEmpty() && !tfType.getText().isEmpty() && !tfPrice.getText().isEmpty() && !tfQty.getText().isEmpty()) {
            String brand = tfBrand.getText();
            String model = tfModel.getText();

            String type = tfType.getText();
            Double price = Double.valueOf(tfPrice.getText());
            int qty = Integer.valueOf(tfQty.getText());

            Product.ProductBuilder builder = new Product.ProductBuilder();
            Product product = builder
                    .setId(UUID.randomUUID().toString())
                    .setBrand(brand)
                    .setModel(model)
                    .setPrice(price)
                    .setType(type)
                    .setQty(qty)
                    .build();

            AccessProduct.saveProduct(product);
            selectedProductTf.setText("None");
            loadTable();
            tfBrand.setText("");
            tfPrice.setText("");
            tfModel.setText("");
            tfQty.setText("");
            tfType.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Form is Incomplete", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuild1ActionPerformed

