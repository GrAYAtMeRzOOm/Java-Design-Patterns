    public double CalculateMethod() {

        ArithmaticValue value = new Number("0");

        for (int i = 0; i < valueList.size(); i++) {
            String get = valueList.get(i);
            switch (get) {
                case "+": {
                    ArithmaticValue nextValue = new Number(valueList.get(i + 1));
                    value = new PlusOperator(value, nextValue);
                    i++;
                    break;
                }
                case "-": {
                    ArithmaticValue nextValue = new Number(valueList.get(i + 1));
                    value = new MinusOperator(value, nextValue);
                    i++;
                    break;
                }
                case "*": {
                    ArithmaticValue nextValue = new Number(valueList.get(i + 1));
                    value = new AstrixOperator(value, nextValue);
                    i++;
                    break;
                }
                case "/": {
                    ArithmaticValue nextValue = new Number(valueList.get(i + 1));
                    value = new BackSlashOperator(value, nextValue);
                    i++;
                    break;
                }
                default:
                    value = new Number(get);
                    break;
            }
        }
        return value.interpretSum();
    }

