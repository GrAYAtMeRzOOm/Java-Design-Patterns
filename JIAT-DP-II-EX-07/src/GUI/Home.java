    private void equal_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equal_btnActionPerformed
        try {
            if (!answerDisplay.getText().equals("")) {
                String Text = answerDisplay.getText();

                char[] toCharArray = Text.toCharArray();
                String num = "";

                for (char u : toCharArray) {
                    String value = String.valueOf(u);
                    switch (value) {
                        case "+":
                            valueList.add(num);
                            valueList.add("+");
                            num = "";
                            break;
                        case "-":
                            valueList.add(num);
                            valueList.add("-");
                            num = "";
                            break;
                        case "*":
                            valueList.add(num);
                            valueList.add("*");
                            num = "";
                            break;
                        case "/":
                            valueList.add(num);
                            valueList.add("/");
                            num = "";
                            break;
                        default:
                            num += value;
                            break;
                    }
                }
                valueList.add(num);
                double CalculateMethod = CalculateMethod();
                answerDisplay.setText(Text);
                activityDisplay.setText(String.valueOf(CalculateMethod));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Operators are Mismatched", "Mathematical error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_equal_btnActionPerformed

