    private void clearTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearTableActionPerformed
        if (jTable1.getRowCount() > 0) {

            tot = 0.0;
            subTot = 0.0;
            dp = 0.0;

            rowCount.setText(String.valueOf(dtm.getRowCount()));
            jLabel31.setText(String.valueOf(subTot));
            jLabel30.setText(String.valueOf(dp));
            jLabel29.setText(String.valueOf(tot));

            dtm.setRowCount(0);
            backgroundUpdate();
        } else {
            JOptionPane.showMessageDialog(this, "Table Already Clear", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_clearTableActionPerformed

