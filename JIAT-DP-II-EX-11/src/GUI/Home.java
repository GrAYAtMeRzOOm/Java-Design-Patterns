    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String brand = jTextField1.getText();
        String model = jTextField2.getText();
        String ram = jTextField3.getText();
        String graphic = jTextField4.getText();

        if (brand.isEmpty() || model.isEmpty() || ram.isEmpty() || graphic.isEmpty() || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Some Fileds are Empty", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            double price = Double.parseDouble(jTextField5.getText());
            double delivery = Double.parseDouble(jTextField6.getText());
            Laptop laptop = new Laptop(brand, model, ram, graphic, price, delivery);

            Vector v = new Vector();
            v.add(brand);
            v.add(model);
            v.add(laptop.toString());
            v.add(laptop.getPrice());
            v.add(laptop.getDelivery());
            v.add("Laptop");

            tot = tot + c.visit(laptop);
            subTot = subTot + price;
            dp = dp + delivery;

            dtm.addRow(v);
            brandList.add(brand);
        }
        backgroundUpdate();
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

